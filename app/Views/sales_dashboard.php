$formatMoney = static function ($value, $currency = 'EUR') {
    return number_format((float)$value, 2, ',', '.') . ' ' . htmlspecialchars((string)$currency);
};

$renderLabel = static function ($group, $key) {
    static $controller = null;
    if ($controller === null) {
        $controller = new SalesController();
    }
    return $controller->translateLabel($group, $key);
};

$pageSize = 5;
$queryParams = $_GET;

$buildSalesUrl = static function (array $params = [], string $fragment = '') use ($queryParams): string {
    $query = $queryParams;
    foreach ($params as $key => $value) {
        if ($value === null || $value === '') {
            unset($query[$key]);
            continue;
        }
        $query[$key] = (string)$value;
    }

    $queryString = http_build_query($query);
    return '/sales' . ($queryString !== '' ? '?' . $queryString : '') . ($fragment !== '' ? '#' . $fragment : '');
};

$buildCreateUrl = static function (string $type, array $params = []): string {
    $query = [];
    foreach ($params as $key => $value) {
        if ($value === null || $value === '' || (int)$value === 0) {
            continue;
        }
        $query[$key] = (string)$value;
    }

    $queryString = http_build_query($query);
    return '/sales/' . $type . '/create' . ($queryString !== '' ? '?' . $queryString : '');
};

$paginateRows = static function (array $rows, string $pageParam) use ($pageSize): array {
    $totalItems = count($rows);
    $totalPages = max(1, (int)ceil($totalItems / $pageSize));
    $currentPage = max(1, (int)($_GET[$pageParam] ?? 1));
    if ($currentPage > $totalPages) {
        $currentPage = $totalPages;
    }

    $offset = ($currentPage - 1) * $pageSize;

    return [
        'items' => array_slice($rows, $offset, $pageSize),
        'page' => $currentPage,
        'page_param' => $pageParam,
        'total_items' => $totalItems,
        'total_pages' => $totalPages,
        'start_item' => $totalItems > 0 ? $offset + 1 : 0,
        'end_item' => $totalItems > 0 ? min($offset + $pageSize, $totalItems) : 0,
    ];
};

